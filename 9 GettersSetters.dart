void main(){
  final cuadrado = new Cuadrado(5);
  
  print('area: ${cuadrado.calculaArea()}');
  print('lado: ${cuadrado.lado}');
}

class Cuadrado{
  double lado = 0;  //Lado * Lado
  
  Cuadrado( double lado):
    this.lado = lado;
   
  double calculaArea(){
    return this.lado * lado;
  }
}

